/* Template literals are the most common way to format strings in modern JavaScript. They use backticks (`) and allow for embedded expressions 
using ${}.*/

let name = "Alice";
let age = 25;

let greeting = `Hello, my name is ${name} and I am ${age} years old.`;
console.log(greeting);  

// Output 
/*
Hello, my name is Alice and I am 25 years old.
*/